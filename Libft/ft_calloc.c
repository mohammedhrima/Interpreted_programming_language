#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void			*arr;
	unsigned char	*ptr;
	size_t			i;

	if (count && size > SIZE_MAX / count)
		return (NULL);
	arr = (void *)malloc(count * size);
	if (!arr)
		return (NULL);
	i = 0;
	ptr = (unsigned char *)arr;
	while (i < count * size)
	{
		ptr[i] = 0;
		i++;
	}
	return (arr);
}
